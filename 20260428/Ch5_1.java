public class Ch5_1 {
	public static void main (String [] args) {
		for (int i = 0 ; i <= 9 ; i ++ ) {
			for(int j = 9 ; j >= 0 ; j -- ) {
				System.out.println("i = " + i) ;
				System.out.println("j = " + j) ;
				System.out.println("i * j = " + i*j) ;
				System.out.println("=========================") ;
			}
		}
	}
}
