public class Ch2_1 {
	public static void main (String [] args) {
		System.out.println("Hello") ;
		byte  A1 = 10 ; // byte => 可以存8bits => -128~127 
		System.out.println(A1) ;
		short A2 =12345 ; // short => 可以存16bits => -32768~32767 
		System.out.println(A2) ;
		int A3 = 1357924680 ; // int => 可以存32bits => -2147483648~2147483647 
		System.out.println(A3) ; 
		long A4 = 136652855544614624L ; // long => 可以存64bits => -9223372036854775808~9223372036854775807 
		System.out.println(A4) ;
		double A5 = 3.1415926d ; // double => 可以存64bits => 精度14個小數點
		System.out.println(A5) ;
		float A6 = 31.4f ; // float => 可以存32bits => 精度7個小數點
		System.out.println(A6) ;
		
	}
}
